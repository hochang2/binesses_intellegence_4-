def get_jd(company: str) -> dict | None:
    """기업명으로 JD 정보 조회. 없으면 None 반환."""
    return JD_DB.get(company)


def get_jd_summary(company: str) -> str:
    """
    프롬프트에 삽입할 JD 요약 텍스트 생성.
    기업 데이터 없으면 빈 문자열 반환.
    """
    jd = JD_DB.get(company)
    if not jd:
        return ""

    lines = [
        f"## {company} 인재상 · 핵심역량\n",
        f"**인재상**: {jd['인재상']}\n",
        f"**핵심역량**: {', '.join(jd['핵심역량'])}\n",
        f"**자소서 작성 팁**:",
    ]
    for tip in jd["자소서팁"]:
        lines.append(f"- {tip}")

    return "\n".join(lines)
